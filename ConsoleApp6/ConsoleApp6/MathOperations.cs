using System;

public class MathOperations
{
    // Void method that takes two integers as parameters, performs a math operation, and displays the second integer
    public void PerformMathOperation(int number1, int number2)
    {
        // For demonstration, this method adds the two numbers
        int result = number1 + number2;
        Console.WriteLine($"Result: {result}");
    }
}
